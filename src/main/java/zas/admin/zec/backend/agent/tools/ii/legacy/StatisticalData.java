    public static List<Integer> toRange(String id) {
        List<Integer> idRange = new ArrayList<>();

        if (id.contains("+")) {
            // exactly matches your Python hard‑coded branch
            idRange = Arrays.asList(77, 79, 80, 81, 82);

        } else if (id.contains("/")) {
            if (id.contains("-")) {
                // e.g. "05-09/35-39"
                String[] ranges = id.split("/");
                for (String r : ranges) {
                    String[] ends = r.split("-");
                    int start = Integer.parseInt(ends[0]);
                    int end = Integer.parseInt(ends[ends.length - 1]);
                    for (int i = start; i <= end; i++) {
                        idRange.add(i);
                    }
                }
            } else {
                // e.g. "64/66"
                for (String part : id.split("/")) {
                    idRange.add(Integer.parseInt(part));
                }
            }

        } else if (id.contains("-")) {
            // e.g. "05-43"
            String[] ends = id.split("-");
            int start = Integer.parseInt(ends[0]);
            int end = Integer.parseInt(ends[ends.length - 1]);
            for (int i = start; i <= end; i++) {
                idRange.add(i);
            }

        } else {
            // single value
            idRange.add(Integer.parseInt(id));
        }

        return idRange;
    }

    public static Map<String, List<Map<String, Object>>> loadT1Index() {
        String[] targets = {"hf", "rai"};
        Map<String, List<Map<String, Object>>> t1Index = new HashMap<>();

        for (String target : targets) {
            List<Map<String, Object>> index = new ArrayList<>();
            String[] ids = loadIds(target);
            for (String id : ids) {
                Map<String, Object> entry = new HashMap<>();
                entry.put("id", id);
                entry.put("index", toRange(id));
                index.add(entry);
            }

            if (target.equals("hf")) {
                t1Index.put("homme", index);
                t1Index.put("femme", index);
            } else {
                t1Index.put("26 al. 6 RAI", index);
            }
        }

        return t1Index;
    }

